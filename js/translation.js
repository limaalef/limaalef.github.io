const LanguageManager = {
    currentLang: 'pt-BR',

    init() {
        // Detectar idioma do navegador/sistema
        const browserLang = navigator.language || navigator.userLanguage;
        
        // Verificar se há idioma salvo anteriormente
        const savedLang = window.appLang;
        
        let defaultLang = 'pt-BR';
        
        // Se não há idioma salvo, usar detecção automática
        if (!savedLang) {
            // Se o idioma do navegador começa com 'pt' (pt-BR, pt-PT, etc), usar português
            // Caso contrário, usar inglês
            defaultLang = browserLang.toLowerCase().startsWith('pt') ? 'pt-BR' : 'en';
        } else {
            // Usar idioma salvo
            defaultLang = savedLang;
        }
        
        this.setLanguage(defaultLang, false); // false = não re-renderizar ainda
        
        document.getElementById('langToggle').addEventListener('click', () => {
            const newLang = this.currentLang === 'pt-BR' ? 'en' : 'pt-BR';
            this.setLanguage(newLang);
        });
    },

    setLanguage(lang, rerender = true) {
        this.currentLang = lang;
        window.appLang = lang;
        document.getElementById('langFlag').textContent = lang === 'pt-BR' ? '🇧🇷' : '🇺🇸';
        this.updateAllTexts(rerender);
    },

    t(key) {
        return translations[this.currentLang][key] || key;
    },

    updateAllTexts(rerender = true) {
        // Atualiza elementos com data-i18n
        document.querySelectorAll('[data-i18n]').forEach(el => {
            const key = el.getAttribute('data-i18n');
            const translation = this.t(key);
            
            if (el.tagName === 'INPUT') {
                el.placeholder = translation;
            } else if (el.tagName === 'OPTION') {
                el.textContent = translation;
            } else {
                el.textContent = translation;
            }
        });

        // Atualiza placeholders especiais
        document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
            const key = el.getAttribute('data-i18n-placeholder');
            el.placeholder = this.t(key);
        });

        // Atualiza info de página
        this.updatePageInfo();

        // Re-renderiza apenas se tiver dados E rerender for true
        if (rerender && window.AppState && AppState.filteredMatches && AppState.filteredMatches.length > 0) {
            if (window.UI && UI.renderMatches) {
                UI.renderMatches();
            }
        }
    },

    updatePageInfo() {
        const pageInfo = document.getElementById('pageInfo');
        if (pageInfo && window.AppState) {
            pageInfo.textContent = `${this.t('page')} ${AppState.currentPage} ${this.t('of')} ${AppState.totalPages}`;
        }
    },

    translateText(text) {
        if (!text) return text;
        
        // Tenta encontrar tradução exata
        const translation = this.t(text);
        if (translation !== text) return translation;
        
        // Se não encontrar tradução exata, tenta traduzir partes conhecidas
        // Exemplo: "28ª Rodada" -> "28th Round"
        if (this.currentLang === 'en') {
            // Traduz padrões de rodadas
            if (text.includes('Disputa 3º Lugar')) {
                return `Play-off for 3rd place`;
            }
            
            let leg = '';
            if (text.includes('Ida')) {
                leg = ' - 1st Leg';
            } else if (text.includes('Volta')) {
                leg = ' - 2nd Leg';
            }

            if (text.includes('Oitavas de Final')) {
                return `Round of 16${leg}`;
            } else if (text.includes('Quartas de Final')) {
                return `Quarterfinals${leg}`;
            } else if (text.includes('Semifinal') || text.includes('Semifinais')) {
                return `Semifinals${leg}`;
            } else if (text.includes('Final')) {
                return `Final${leg}`;
            }

            const roundMatch = text.match(/(\d+)ª Rodada/);
            if (roundMatch) {
                const number = roundMatch[1];
                const suffix = this.getOrdinalSuffix(number);
                return `Round ${number}`;
            }
        }
        
        return text;
    },
    
    getOrdinalSuffix(num) {
        const n = parseInt(num);
        const s = ["th", "st", "nd", "rd"];
        const v = n % 100;
        return s[(v - 20) % 10] || s[v] || s[0];
    }
};